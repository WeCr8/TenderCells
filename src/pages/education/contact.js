export function initializeEducationContactPage() {
  // Community form submission
  const communityForm = document.querySelector('.community-form');
  if (communityForm) {
    communityForm.addEventListener('submit', (e) => {
      e.preventDefault();
      const formData = new FormData(e.target);
      const data = Object.fromEntries(formData);
      
      alert(`Welcome to the Tender Cells educator community, ${data.communityName}! You'll receive a welcome email with login instructions within 24 hours.`);
      e.target.reset();
    });
  }
  
  // Support contact form submission
  const supportForm = document.querySelector('.support-contact-form');
  if (supportForm) {
    supportForm.addEventListener('submit', (e) => {
      e.preventDefault();
      const formData = new FormData(e.target);
      const data = Object.fromEntries(formData);
      
      const urgencyMap = {
        'urgent': 'within 1 hour',
        'high': 'within 4 hours',
        'medium': 'within 24 hours',
        'low': 'within 48 hours'
      };
      
      const responseTime = urgencyMap[data.contactUrgency] || 'within 48 hours';
      
      alert(`Thank you for contacting us! We've received your message about "${data.contactSubject}" and will respond ${responseTime}.`);
      e.target.reset();
    });
  }
  
  console.log('Education Contact page initialized');
}